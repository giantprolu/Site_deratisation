// Générateur de mots-clés dynamiques
export const generateLocationKeywords = (service: string, department?: string) => {
  const baseKeywords = [service];
  
  if (department && department in seoData.cities) {
    const cities = seoData.cities[department as keyof typeof seoData.cities];
    cities.forEach((city: string) => {
      baseKeywords.push(`${service} ${city}`);
    });
  }
  
  // Ajouter tous les arrondissements de Paris
  seoData.cities['75'].forEach((arrondissement: string) => {
    baseKeywords.push(`${service} ${arrondissement}`);
  });
  
  return baseKeywords;
};

// Générateur de schema FAQ dynamique
export const generateFAQSchema = (serviceType: keyof typeof seoData.faqs = 'general') => {
  return {
    '@context': 'https://schema.org',
    '@type': 'FAQPage',
    mainEntity: seoData.faqs[serviceType].map(faq => ({
      '@type': 'Question',
      name: faq.question,
      acceptedAnswer: {
        '@type': 'Answer',
        text: faq.answer
      }
    }))
  };
};